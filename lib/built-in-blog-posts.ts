function normalizeText(value?: string | null) {
  return (value || '').trim().toLowerCase()
}

export function isPlaceholderBlogPost(post: BlogPostRecord) {
  const slug = normalizeText(post.slug)
  const title = normalizeText(post.title)
  const excerpt = normalizeText(post.excerpt)

  return (
    slug === PLACEHOLDER_BLOG_SLUG ||
    title === 'добро пожаловать в seo services' ||
    excerpt.includes('первая статья в нашем блоге')
  )
}

const builtInBlogPostsMap = new Map<string, BlogPostRecord>(
  builtInBlogPosts
    .filter((post): post is BlogPostRecord & { slug: string } => typeof post.slug === 'string')
    .map((post) => [post.slug, post] as const)
)

export function getBuiltInBlogPostBySlug(slug: string) {
  return builtInBlogPostsMap.get(slug) || null
}

export function hydrateBlogPostRecord<T extends BlogPostRecord>(post: T): T {
  if (!post.slug) {
    return post
  }

  const builtInPost = getBuiltInBlogPostBySlug(post.slug)
  if (!builtInPost) {
    return post
  }

  return {
    ...post,
    title: builtInPost.title,
    excerpt: builtInPost.excerpt,
    content: builtInPost.content,
    coverImage: builtInPost.coverImage || post.coverImage || null,
    published: typeof post.published === 'boolean' ? post.published : builtInPost.published,
    publishedAt: post.publishedAt || builtInPost.publishedAt,
    updatedAt: post.updatedAt || builtInPost.updatedAt || builtInPost.publishedAt,
  }
}

export function buildBlogListing<T extends BlogPostRecord>(posts: T[]) {
  const visiblePosts = posts
    .filter((post) => !isPlaceholderBlogPost(post))
    .map((post) => hydrateBlogPostRecord(post))

  const existingSlugs = new Set(visiblePosts.map((post) => post.slug).filter(Boolean))
  const missingBuiltIns = builtInBlogPosts
    .filter((post) => !existingSlugs.has(post.slug))
    .map((post) => ({
      id: `built-in-${post.slug}`,
      ...post,
    })) as T[]

  return [...visiblePosts, ...missingBuiltIns].sort((left, right) => {
    const leftDate = left.publishedAt ? new Date(left.publishedAt).getTime() : 0
    const rightDate = right.publishedAt ? new Date(right.publishedAt).getTime() : 0

    return rightDate - leftDate
  })
}
